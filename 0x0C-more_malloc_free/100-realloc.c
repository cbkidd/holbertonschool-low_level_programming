#include "holberton.h"

/**
* _realloc - does stuff
* @ptr: pointer
* @old_size: old size
* @new_size: new size
*
*
*
*
*
* Return: null
*/

void *_realloc(void *ptr, unsigned int old_size, unsigned int new_size)
{
if (ptr != '\0')
	ptr = 0;

if (old_size != '\0')
	old_size = 0;

if (new_size != '\0')
	new_size = 0;
return (NULL);
}
