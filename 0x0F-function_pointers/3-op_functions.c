#include "3-calc.h"

/**
* op_add - add ints
* @a: int a
* @b: int b
* Return: a + b
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - subtract ints
* @a: int a
* @b: int b
* Return: a - b
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul - multipy ints
* @a: int a
* @b: int b
* Return: a * b
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div - divides ints
* @a: int a
* @b: int b
* Return:a / b
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - modulos' ints
* @a: int a
* @b: int b
* Return: a % b
*/

int op_mod(int a, int b)
{
return (a % b);
}
